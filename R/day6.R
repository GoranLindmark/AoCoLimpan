allCharsUnique <- function(str) {
  str <- unlist(strsplit(str, ""))
  res <- list()
  res[1] <- !(str[1] %in% str[2:4])
  res[2] <- !(str[4] %in% str[1:3])
  res[3] <- !(str[2] %in% c(str[1], str[3:4]))
  res[4] <- !(str[3] %in% c(str[1:2], str[4]))

  notFound <- all(unlist(res))
  return(notFound)
}

